let number = 5.9;
const roundnumber = Math.ceil(number); //always rounds up and returns the smallest integer greater than or equal to a given number.
console.log(roundnumber);

////MATHS////
console.log(Math.abs(-4)); //gives positive value
console.log(Math.round(4.6)); // number rounded to the nearest integer.
console.log(Math.floor(4.6)); //always rounds down and returns the largest integer less than or equal to a given number
console.log(Math.max(4, 6, 8, 9, 2));
console.log(Math.min(4, 6, 8, 9, 2));
console.log(Math.random() * 10 + 1); //returns a floating-point, pseudo-random number that's greater than or equal to 0 and less than 1
